// Zarezerwowane miejsce z określonym typem danych
public class Zmienne {

    public static void main(String[] args) {
        int firstNumber, secondNumber, result; // deklaracja zmiennych
        String name = "Bartosz"; // [typ danych] [nazwa zmiennej]
        firstNumber = 1;
        secondNumber = 2;
        result = firstNumber + secondNumber;
        name = "Wojciech"; // zmienne można nadpisywać
        System.out.println(name);
        System.out.println(result);
        // zmienne przed wywołaniem muszą mieć przypisane wartości
    }
}
